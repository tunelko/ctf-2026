# ============================================================
# DNS Protocol Helpers
# ============================================================

TYPE_MAP = {"A": 1, "NS": 2, "CNAME": 5, "SOA": 6, "MX": 15, "TXT": 16, "AAAA": 28, "DNSKEY": 48, "RRSIG": 46}
RTYPE_MAP = {v: k for k, v in TYPE_MAP.items()}


def encode_name(name):
    buf = bytearray()
    for label in name.rstrip(".").split("."):
        buf += bytes([len(label)]) + label.encode()
    buf += b"\x00"
    return bytes(buf)


def encode_rdata(rtype, rdata):
    if rtype == "A":
        return bytes([int(p) for p in rdata.split(".")])
    elif rtype == "TXT":
        encoded = rdata.encode()
        result = bytearray()
        for i in range(0, len(encoded), 255):
            chunk = encoded[i:i+255]
            result += bytes([len(chunk)]) + chunk
        return bytes(result)
    elif rtype == "NS":
        return encode_name(rdata)
    elif rtype == "MX":
        parts = rdata.split(None, 1)
        return struct.pack("!H", int(parts[0])) + encode_name(parts[1])
    elif rtype == "SOA":
        parts = rdata.split()
        wire = encode_name(parts[0]) + encode_name(parts[1])
        wire += struct.pack("!IIIII", *[int(p) for p in parts[2:7]])
        return wire
    return rdata.encode()


def encode_rrsig_rdata(rrsig, signer_name):
    """Encode an RRSIG record in DNS wire format (RFC 4034 Section 3.1)."""
    type_covered = rtype_to_int(rrsig["type_covered"])
    algorithm = rrsig["algorithm"]
    labels = rrsig.get("labels", len(signer_name.rstrip(".").split(".")))
    original_ttl = rrsig.get("original_ttl", 3600)
    expiration = rrsig["expiration"]
    inception = rrsig["inception"]
    key_tag = rrsig["key_tag"]
    sig_bytes = base64.b64decode(rrsig["signature"])

    wire = struct.pack("!HBBI", type_covered, algorithm, labels, original_ttl)
    wire += struct.pack("!I", expiration)
    wire += struct.pack("!I", inception)
    wire += struct.pack("!H", key_tag)
    wire += encode_name(signer_name)
    wire += sig_bytes

    return wire


def encode_dnskey_rdata(dnskey):
    """Encode DNSKEY RDATA from zone data."""
    return base64.b64decode(dnskey["public_key"])


def build_dns_response(query_data, zone_data):
    """Parse DNS query and build response with signed records."""

    # Parse header
    txid = struct.unpack("!H", query_data[:2])[0]
    flags = struct.unpack("!H", query_data[2:4])[0]

    include_rrsig = True

    # Parse question
    offset = 12
    labels = []
    while offset < len(query_data) and query_data[offset] != 0:
        length = query_data[offset]
        offset += 1
        labels.append(query_data[offset:offset + length].decode().lower())
        offset += length
    offset += 1  # null terminator

    if offset + 4 > len(query_data):
        return None

    qname = ".".join(labels) + "."
    qtype_raw = struct.unpack("!H", query_data[offset:offset + 2])[0]
    qclass = struct.unpack("!H", query_data[offset + 2:offset + 4])[0]
    qtype = RTYPE_MAP.get(qtype_raw, "UNKNOWN")

    # Build response
    response = bytearray()
    answers = []

    # Handle DNSKEY query
    if qname.lower() == ZONE_NAME.lower() and qtype in ("DNSKEY", "ANY"):
        dnskey = zone_data["dnskey"]
        dnskey_rdata = encode_dnskey_rdata(dnskey)

        answers.append(("DNSKEY", encode_name(qname), 48, dnskey["ttl"], dnskey_rdata))

        if include_rrsig:
            rrsig_rdata = encode_rrsig_rdata(dnskey["rrsig"], ZONE_NAME)
            answers.append(("RRSIG", encode_name(qname), 46, dnskey["ttl"], rrsig_rdata))

    # Handle regular record queries
    for rec in zone_data["records"]:
        if rec["name"].lower() == qname.lower() and (qtype == "ANY" or rec["type"] == qtype):
            name_wire = encode_name(rec["name"])
            type_num = TYPE_MAP.get(rec["type"], 16)

            for rdata_str in rec["rdata_list"]:
                rdata_wire = encode_rdata(rec["type"], rdata_str)
                answers.append((rec["type"], name_wire, type_num, rec["ttl"], rdata_wire))

            # Include RRSIG
            if include_rrsig and "rrsig" in rec:
                rrsig_rdata = encode_rrsig_rdata(rec["rrsig"], ZONE_NAME)
                answers.append(("RRSIG", name_wire, 46, rec["ttl"], rrsig_rdata))

    # Header
    an_count = len(answers)
    response += struct.pack("!H", txid)
    response += struct.pack("!H", 0x8400 if an_count > 0 else 0x8403)
    response += struct.pack("!H", 1)          # QDCOUNT
    response += struct.pack("!H", an_count)   # ANCOUNT
    response += struct.pack("!H", 0)          # NSCOUNT
    response += struct.pack("!H", 0)          # ARCOUNT

    # Question (echo back)
    response += query_data[12:offset + 4]

    # Answers
    for (rtype_str, name_wire, type_num, ttl, rdata_wire) in answers:
        response += name_wire
        response += struct.pack("!H", type_num)
        response += struct.pack("!H", 1)              # CLASS IN
        response += struct.pack("!I", ttl)
        response += struct.pack("!H", len(rdata_wire))
        response += rdata_wire

    return bytes(response)


# ============================================================
# RRSIG Verification
# ============================================================

def verify_rrsig(zone_data, name, rtype, ttl, rdata, signature_b64):
    """Verify a submitted RRSIG against the zone's DNSKEY."""
    dnskey_rdata = base64.b64decode(zone_data["dnskey"]["public_key"])
    pub_raw = dnskey_rdata[4:]

    pub_uncompressed = b"\x04" + pub_raw
    public_key = ec.EllipticCurvePublicKey.from_encoded_point(ec.SECP256R1(), pub_uncompressed)

    inception = zone_data["inception"]
    expiration = zone_data["expiration"]
    key_tag = zone_data["dnskey"]["key_tag"]

    rrsig_header = build_rrsig_header(rtype, int(ttl), inception, expiration, key_tag, ZONE_NAME)
    rrset_wire = build_rrset_wire(name, rtype, int(ttl), [rdata])

    data_to_verify = rrsig_header + rrset_wire

    try:
        sig_raw = base64.b64decode(signature_b64)
        if len(sig_raw) != 64:
            return False, "Signature must be 64 bytes (r||s format per RFC 6605)"
        r = int.from_bytes(sig_raw[:32], "big")
        s = int.from_bytes(sig_raw[32:], "big")
        der_sig = encode_dss_signature(r, s)
    except Exception as e:
        return False, f"Invalid signature encoding: {e}"

    try:
        public_key.verify(der_sig, data_to_verify, ec.ECDSA(hashes.SHA256()))
        return True, "RRSIG verified successfully"
    except Exception:
        return False, "RRSIG verification failed - signature does not match"


