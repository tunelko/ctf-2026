# HTTP API Handler
# ============================================================

class DNSSECAPIHandler(BaseHTTPRequestHandler):
    """
    Minimal HTTP API for external RRSIG validation.
    POST /verify  — submit an RRSIG for a given RRset
    """

    def __init__(self, zone_data, request, client_address, server):
        self.zone_data = zone_data
        super().__init__(request, client_address, server)

    def do_GET(self):
        self._respond(200, {
            "service": "xstf.pt DNSSEC server",
            "version": "1.0",
            "api": {
                "POST /verify": "Validate an RRSIG (params: name, type, ttl, rdata, sig)",
            },
        })

    def do_POST(self):
        if self.path != "/verify":
            self._respond(404, {"error": "Not found"})
            return

        content_length = int(self.headers.get("Content-Length", 0))
        body = self.rfile.read(content_length).decode()
        params = parse_qs(body)

        name  = params.get("name",  [""])[0].strip()
        rtype = params.get("type",  [""])[0].strip().upper()
        ttl   = params.get("ttl",   [""])[0].strip()
        rdata = params.get("rdata", [""])[0].strip()
        sig   = params.get("sig",   [""])[0].strip()

        if not all([name, rtype, ttl, rdata, sig]):
            self._respond(400, {"error": "Missing fields. Required: name, type, ttl, rdata, sig"})
            return

        # Only accept validation requests for flag.xstf.pt
        if name.lower().rstrip(".") + "." != "flag.xstf.pt.":
            self._respond(400, {"error": "Submission must target flag.xstf.pt."})
            return

        name = "flag.xstf.pt."

        valid, message = verify_rrsig(self.zone_data, name, rtype, int(ttl), rdata, sig)

        if valid:
            self._respond(200, {
                "status": "RRSIG_VERIFIED",
                "message": "Signature accepted. The resolver trusts your record.",
                "flag": FLAG,
            })
        else:
            self._respond(403, {
                "status": "SERVFAIL",
                "error": message,
            })

    def _respond(self, code, data):
        self.send_response(code)
        self.send_header("Content-Type", "application/json")
        self.end_headers()
        self.wfile.write(json.dumps(data, indent=2).encode())

    def log_message(self, format, *args):
        print(f"[HTTP] {self.client_address[0]} - {format % args}")


# ============================================================
# Multiplexed TCP handler: DNS-over-TCP vs HTTP auto-detection
# ============================================================

HTTP_METHODS = (b"GET ", b"POST", b"HEAD", b"PUT ", b"DELE", b"OPTI", b"PATC")


class MultiplexHandler(socketserver.BaseRequestHandler):
    """
    Accepts a TCP connection and peeks at the first bytes:
      - HTTP method prefix -> route to DNSSECAPIHandler
      - Otherwise         -> DNS-over-TCP (2-byte length + DNS wire)
    """

    def handle(self):
        try:
            peek = self.request.recv(4, socket.MSG_PEEK)
            if not peek:
                return

            if any(peek.startswith(m) for m in HTTP_METHODS):
                self._handle_http()
            else:
                self._handle_dns_tcp()
        except Exception as e:
            print(f"[MUX] Error from {self.client_address}: {e}", file=sys.stderr)

    def _handle_dns_tcp(self):
        """Handle DNS over TCP (RFC 7766): 2-byte length prefix + DNS message."""
        try:
            length_data = self._recv_exact(2)
            if not length_data:
                return
            msg_len = struct.unpack("!H", length_data)[0]

            dns_data = self._recv_exact(msg_len)
            if not dns_data:
                return

            response = build_dns_response(dns_data, self.server.zone_data)
            if response:
                self.request.sendall(struct.pack("!H", len(response)) + response)
        except Exception as e:
            print(f"[DNS-TCP] Error: {e}", file=sys.stderr)

    def _handle_http(self):
        """Hand off to the HTTP handler."""
        try:
            DNSSECAPIHandler(
                self.server.zone_data,
                self.request,
                self.client_address,
                self.server,
            )
        except Exception as e:
            print(f"[HTTP] Error: {e}", file=sys.stderr)

    def _recv_exact(self, n):
        """Read exactly n bytes from socket."""
        buf = b""
        while len(buf) < n:
            chunk = self.request.recv(n - len(buf))
            if not chunk:
                return None
            buf += chunk
        return buf


class MultiplexServer(socketserver.ThreadingTCPServer):
    allow_reuse_address = True
    daemon_threads = True

    def __init__(self, address, handler_class, zone_data):
        self.zone_data = zone_data
        super().__init__(address, handler_class)


